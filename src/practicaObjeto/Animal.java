
package practicaObjeto;

public class Animal {
    // atributos
  private String raza;
  private String color;
  private int edad;
  private int peso;

    public String getRaza() {
        return raza;
    }

    public void setRaza(String raza) {
        this.raza =raza;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public int getPeso() {
        return peso;
    }

    public void setPeso(int peso) {
        this.peso = peso;
    }

}
